export type RoomType =
  | "classroom"
  | "lab"
  | "office"
  | "restroom"
  | "lift"
  | "stairs"
  | "utility"
  | "corridor"
  | "floorLabel"
  | "label";

export interface RoomInfo {
  name: string;
  type: RoomType;
}

export interface FloorConfig {
  id: string;
  name: string;
  floorNumber: number;
  columns: number;
  gridTemplate: string[];
  rooms: Record<string, RoomInfo>;
}

const CLICKABLE_TYPES: RoomType[] = ["classroom", "lab", "office", "utility"];

export function isClickable(type: RoomType): boolean {
  return CLICKABLE_TYPES.includes(type);
}

export function getRoomTypeLabel(type: RoomType): string {
  switch (type) {
    case "classroom":
      return "Classroom";
    case "lab":
      return "Lab";
    case "office":
      return "Office";
    case "utility":
      return "Utility";
    default:
      return type;
  }
}

